// - Math.PI : in số pi
console.log(Math.round);

// - Math.round() : làm tròn
console.log(Math.round(1.5));

// - Math.abs() : chuyển thành số dương
console.log(Math.abs(-4));

// - Math.ceil() : làm tròn lên
console.log(Math.ceil(4.1111));

// - Math.floor() : làm tròn xuống
console.log(Math.floor(4.9999));

// - Math.random() : random
console.log(Math.floor(Math.random() * 10));

// - Math.min() : in số nhỏ nhất
console.log(Math.min(-100, 1, 90, 50, 40));

// - Math.max() : in số lớn nhất
console.log(Math.max(-100, 1, 90, 50, 40));
